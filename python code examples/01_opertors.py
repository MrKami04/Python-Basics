#  operators: in python - operators are special symbols that perform operations on variables and values.
# as arithmetic operators are addition, subtraction, multiplication, division, modulus, floor division, exponentiation.
print(2+3)  # addition
print(5-3)  # subtraction
print(4*3)  # multiplication
print(4/2)  # division

print(4%3)  # modulus
print(4//2)  # floor division
print(4**3)  # exponentiation
